export function getEncontroById(id) {
  return encontros.find((e) => e.numero === Number(id))
}

export function getEncontroAtual(referencia = new Date()) {
  const hoje = new Date(referencia)
  hoje.setHours(0, 0, 0, 0)

  const passadosComConteudo = gerarCalendarioEncontros()
    .map((c) => ({
      ...c,
      dataObj: new Date(c.data + 'T00:00:00'),
      conteudo: encontros.find((e) => e.numero === c.numero),
    }))
    .filter((c) => c.dataObj <= hoje && c.conteudo)

  return passadosComConteudo.length > 0
    ? passadosComConteudo[passadosComConteudo.length - 1].conteudo
    : encontros[0]
}

export function getCalendarioComStatus(referencia = new Date()) {
  const hoje = new Date(referencia)
  hoje.setHours(0, 0, 0, 0)
  const encontroAtual = getEncontroAtual(hoje)

  return gerarCalendarioEncontros().map((c) => {
    const dataObj = new Date(c.data + 'T00:00:00')
    const conteudo = encontros.find((e) => e.numero === c.numero)
    let status = 'futuro'

    if (dataObj < hoje) status = 'realizado'
    else if (conteudo && c.numero === encontroAtual.numero) status = 'atual'
    else if (conteudo && dataObj <= hoje) status = 'disponivel'

    return {
      ...c,
      dataObj,
      conteudo,
      conteudoDisponivel: conteudo,
      status,
      passado: status === 'realizado',
    }
  })
}

export function getProximoSabado(referencia = new Date()) {
  const data = new Date(referencia)
  data.setHours(0, 0, 0, 0)
  const diaSemana = data.getDay()
  const diasAteSabado = (6 - diaSemana + 7) % 7 || 7
  data.setDate(data.getDate() + diasAteSabado)
  return data
}

// Recesso dos encontros. Enquanto `ativo` for true, a Home mostra o aviso de
// recesso e o contador passa a contar para a data de `retorno` (um sábado).
// Os sábados que caem dentro do recesso não têm encontro, então entram em
// SABADOS_SEM_ENCONTRO: somem do calendário e a numeração recua (a retomada
// reocupa o próximo número). Ao chegar a data de `retorno`, getRecessoAtivo
// já volta ao comportamento normal sozinho; pode-se também trocar `ativo`
// para false (ou apagar este objeto).
export const RECESSO = {
  ativo: true,
  retorno: '2026-07-11', // sábado da retomada
}

// Retorna os dados do recesso se ele estiver ativo E ainda não tiver chegado a
// data de retorno; caso contrário, null (comportamento normal).
export function getRecessoAtivo(referencia = new Date()) {
  if (!RECESSO.ativo) return null
  const hoje = new Date(referencia)
  hoje.setHours(0, 0, 0, 0)
  const retorno = new Date(RECESSO.retorno + 'T00:00:00')
  if (hoje >= retorno) return null
  return { retorno }
}

// Sábados em que NÃO houve encontro. A sequência "pula" estas datas:
// elas não recebem número nem aparecem no calendário, e os encontros
// seguintes avançam uma semana para reocupar o lugar.
export const SABADOS_SEM_ENCONTRO = [
  '2026-05-30',
  // Recesso de junho/julho: sem encontro nestes sábados. A retomada é 11/07.
  '2026-06-20',
  '2026-06-27',
  '2026-07-04',
]

export function gerarCalendarioEncontros(
  inicio = '2026-05-02',
  total = 17,
  puladas = SABADOS_SEM_ENCONTRO
) {
  const lista = []
  const dataInicio = new Date(inicio + 'T00:00:00')
  let semana = 0
  while (lista.length < total) {
    const data = new Date(dataInicio)
    data.setDate(data.getDate() + semana * 7)
    semana++
    const iso = data.toISOString().slice(0, 10)
    if (puladas.includes(iso)) continue // sábado sem encontro: não conta na numeração
    lista.push({
      numero: lista.length + 1,
      data: iso,
    })
  }
  return lista
}
